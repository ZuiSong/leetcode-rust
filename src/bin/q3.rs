use std::collections::HashSet;

impl Solution {
    pub fn length_of_longest_substring(s: String) -> i32 {
        let s: Vec<_> = s.chars().collect();

        let mut set = HashSet::new();
        let mut ans = 0;
        let mut i = 0;
        let mut ridx = -1;
        while i < s.len() {
            if i != 0 {
                set.remove(&s[i - 1]);
            }

            while ridx + 1 < s.len() as i32 && !set.contains(&s[(ridx + 1) as usize]) {
                set.insert(s[(ridx + 1) as usize]);
                ridx += 1;
            }

            ans = ans.max(ridx - i as i32 + 1);
            i += 1
        }
        ans
    }
}

struct Solution {}

fn main() {
    let res = Solution::length_of_longest_substring("pwwkew".to_string());

    println!("{}", res)
}

